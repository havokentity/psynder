inline const Glyph& glyph_for(char ch) noexcept {
    const auto u = static_cast<u8>(ch);
    if (u < 0x20 || u > 0x7F)
        return kFont[0x3F - 0x20];  // '?'
    return kFont[u - 0x20];
}

inline void draw_glyph(render::Framebuffer& fb, i32 x, i32 y, char ch, u32 colour) noexcept {
    const Glyph& g = glyph_for(ch);
    for (u32 row = 0; row < kGlyphHeight; ++row) {
        const u8 bits = g.rows[row];
        if (bits == 0U)
            continue;
        const i32 py = y + static_cast<i32>(row);
        for (u32 col = 0; col < kGlyphWidth; ++col) {
            const u8 mask = static_cast<u8>(1U << (kGlyphWidth - 1U - col));
            if (bits & mask) {
                plot(fb, x + static_cast<i32>(col), py, colour);
            }
        }
    }
}

// Draws a string left-to-right; returns the advance width in pixels.
inline u32 draw_text(render::Framebuffer& fb, i32 x, i32 y, std::string_view text, u32 colour) noexcept {
    i32 pen = x;
    for (char c : text) {
        draw_glyph(fb, pen, y, c, colour);
        pen += static_cast<i32>(kCellWidth);
    }
    return static_cast<u32>(pen - x);
}

inline u32 text_width(std::string_view text) noexcept {
    return static_cast<u32>(text.size()) * kCellWidth;
}

